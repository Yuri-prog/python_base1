# –í —Ç–µ–∫—É—â–µ–º –º–æ–¥—É–ª–µ —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å –≥–ª–∞–≤–Ω—ã–π —Ü–∏–∫–ª –ø–∞–¥–µ–Ω–∏—è —Å–Ω–µ–∂–∏–Ω–æ–∫,
# –æ–±—Ä–∞—â–∞—è—Å—å –¢–û–õ–¨–ö–û –∫ —Ñ—É–Ω–∫—Ü–∏—è–º –º–æ–¥—É–ª—è snowfall

flake()
k = 0
while True:
    sd.start_drawing()
    color_and_list('—Ñ–æ–Ω')
    if k % 2 == 0:
        move_right()
    else:
        move_left()
    color_and_list('–∂–µ–ª—Ç—ã–π')
    k += 1
    if count() is False:
        break
    sd.sleep(0.1)
    sd.finish_drawing()
    if sd.user_want_exit():
        break
    flake_del()

sd.pause()

