from mastermind_engine import comp_number, take_number, check_match
from termcolor import cprint, colored


def one_more_time():
    while True:
        question = input(colored('–•–æ—Ç–∏—Ç–µ –µ—â–µ –ø–∞—Ä—Ç–∏—é? y/n', color='green'))
        if question == 'y':
            game()
        elif question == 'n':
            print('–í—ã—Ö–æ–¥')
            return
        else:
            cprint('–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –≤–≤–æ–¥, –ø–æ–≤—Ç–æ—Ä–∏—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞.', color='red')
            continue


def game():
    comp_number()
    k = 0
    step_list = []
    while True:
        your_number_string = input(colored('–í–≤–µ–¥–∏—Ç–µ —á–µ—Ç—ã—Ä–µ—Ö–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ: ', color='green'))
        if take_number(your_number_string) is False:
            cprint('–ù–µ–≤–µ—Ä–Ω—ã–π –≤–≤–æ–¥, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑', color='red')
            continue
        cprint('–í—ã –≤–≤–µ–ª–∏ —á–∏—Å–ª–æ {}'.format(your_number_string), color='blue')
        if check_match():
            cprint('–í—ã –≤—ã–∏–≥—Ä–∞–ª–∏!', color='red', on_color='on_green')
            break
        k += 1
        step_list.append(k)
    cprint('–ë—ã–ª–æ –∑–∞–≥–∞–¥–∞–Ω–æ —á–∏—Å–ª–æ {}, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ö–æ–¥–æ–≤ {}'.format(your_number_string, len(step_list) + 1), color='blue')
    one_more_time()


game()

